    private void jLabel2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel2MouseClicked
        jLabel2.setCursor(new java.awt.Cursor(java.awt.Cursor.HAND_CURSOR));
        if (cima) {
            jLabel2.setIcon(ic.redimensionarIcon("baixo", 32, 32));
            cima = false;
        } else {
            jLabel2.setIcon(ic.redimensionarIcon("cima", 32, 32));
            cima = true;
        }
        adicionarItensCardapio();
    }//GEN-LAST:event_jLabel2MouseClicked

    private void adicionarItensCardapio() {
        jPanel3.removeAll();

        GridBagConstraints gbc = new GridBagConstraints();
        gbc.insets = new Insets(10, 10, 10, 10);
        gbc.fill = GridBagConstraints.NONE;
        gbc.weightx = 0;
        gbc.weighty = 0;

        int colunas = 3;
        int linha = 0;
        int coluna = 0;

        List<ProdutoJPA> produtos = LancheDAO.Listar(jCheckBox2.isSelected(), jCheckBox1.isSelected(), cima);

        for (int i = 0; i < produtos.size(); i++) {
            JPanel item = criarItemCardapio(produtos.get(i).getNome(), produtos.get(i).getValor(), produtos.get(i).getImagem());

            gbc.gridx = coluna;
            gbc.gridy = linha;
            jPanel3.add(item, gbc);

            coluna++;
            if (coluna >= colunas) {
                coluna = 0;
                linha++;
            }
        }

        jPanel3.revalidate();
        jPanel3.repaint();
    }

    private JPanel criarItemCardapio(String nome, double preco, String caminhoImagem) {
        JPanel painelItem = new JPanel(new BorderLayout());
        painelItem.setPreferredSize(new java.awt.Dimension(150, 150));

        ImageIcon imagemIcone = new ImageIcon(caminhoImagem);
        JLabel labelImagem = new JLabel();
        labelImagem.setIcon(new ImageIcon(imagemIcone.getImage().getScaledInstance(100, 80, java.awt.Image.SCALE_SMOOTH)));
        labelImagem.setHorizontalAlignment(JLabel.CENTER);
        labelImagem.setBorder(javax.swing.BorderFactory.createEmptyBorder(10, 10, 0, 10));

        JLabel labelNome = new JLabel("<html><center>" + nome + "<br>R$ " + String.format("%.2f", preco) + "</center></html>");
        labelNome.setHorizontalAlignment(JLabel.CENTER);

        painelItem.add(labelImagem, BorderLayout.NORTH);
        painelItem.add(labelNome, BorderLayout.CENTER);
        painelItem.setBorder(javax.swing.BorderFactory.createLineBorder(java.awt.Color.GRAY));

        return painelItem;
    }

