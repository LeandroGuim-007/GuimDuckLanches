    public DefaultTableModel montarTabela() {
        String[] colunas = {"Produto", "Cliente", "Valor", "Data", "Entrega?", "Status"};

        DefaultTableModel tabela = new DefaultTableModel(colunas, 0);

        List<jTableDados> pedidos = PedidoDAO.listar();

        for (jTableDados dados : pedidos) {
            String[] linha = {
                dados.getProduto(),
                dados.getCliente(),
                String.format("R$ %.2f", dados.getValor() * dados.getQuantidade()),
                dados.getData(),
                dados.isParaEntregar() ? "Sim" : "NÃ£o",
                dados.getStatus()
            };
            tabela.addRow(linha);
        }
        return tabela;
    }

