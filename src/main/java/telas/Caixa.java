    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        double caixa = 00.00;  
        String valor = valorText.getText().replace("R$", "").replace(",", ".").trim();

        if ((int) quantidadeSpinner.getValue() == 0) {
            JOptionPane.showMessageDialog(null, "Escolha uma quantidade de produto maior que 0!");
        } else if (dataText.getText().matches("\\d{2}/\\d{2}/\\d{4}") && valorText.getText().isBlank()) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos!");
        } else {
            try {
                PedidoJPA pJPA = new PedidoJPA(dataText.getText(), (String) pagamentoComboBox.getSelectedItem(), "Não finalizado", (int) quantidadeSpinner.getValue(),
                        LancheDAO.getId((String) produtosComboBox.getSelectedItem()), ClienteDAO.getId((String) ClienteComboBox.getSelectedItem()), entregarCheckBox.isSelected());

                PedidoDAO pdao = new PedidoDAO();
                caixa += Double.parseDouble(valor);
                jLabel8.setText(String.format("R$ %.2f", caixa));
                pdao.cadastrar(pJPA);
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(null, "Valor inválido. Digite um número válido.");
            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(null, "Erro ao cadastrar pedido.");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

