private void atualizarValorProduto() {
        double valorProduto = 0.0;

        for (ProdutoJPA p : LancheDAO.Listar(true, true, true)) {
            if (p.getNome().equals(produtosComboBox.getSelectedItem().toString())) {
                valorProduto = p.getValor() * (int) quantidadeSpinner.getValue();
                break;
            }
        }
        valorText.setText(String.format("R$ %.2f", valorProduto));
    }
